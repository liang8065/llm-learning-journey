def get_style_list():
    """获取所有可用的风格列表"""
    return [
        {
            "id": key,
            "name": value["name"],
            "emoji": value["emoji"],
            "description": value["description"]
        }
        for key, value in STYLE_PROMPTS.items()
    ]

def get_prompt(style_id, content):
    """获取指定风格的完整 Prompt"""
    if style_id not in STYLE_PROMPTS:
        raise ValueError(f"未知风格: {style_id}")
    
    style = STYLE_PROMPTS[style_id]
    user_message = style["user_template"].format(content=content)
    
    return {
        "system": style["system"],
        "user": user_message,
        "name": style["name"],
        "emoji": style["emoji"]
    }
